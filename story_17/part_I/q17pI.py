treasure_row = None
treasure_col = None

for r in range(rows):
    for c in range(cols):
        if grid[r][c] == '@':
            treasure_row = r
            treasure_col = c

if treasure_row is None:
    print("No '@' found!")
    exit()

total = 0

for r in range (rows):
    for c in range(cols):

        if r == treasure_row and c == treasure_col:
            continue

        distance = (r - treasure_row)**2 + (c - treasure_col)**2

        if distance <= 10**2:
            if grid[r][c].isdigit():
                total += int(grid[r][c])

print("The answer for PI:", total)
