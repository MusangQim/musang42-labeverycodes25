def fishbone(numbers):
    spine = []
    sides = []
    for num in numbers:
        placed = False
        for i in range(len(spine)):
            s = spine[i]
            left, right = sides[i]
            if num < s and left is None:
                sides[i][0] = num
                placed = True
                break
            elif num > s and right is None:
                sides[i][1] = num
                placed = True
                break
        if not placed:
            spine.append(num)
            sides.append([None, None])
    return int(''.join(str(n) for n in spine))

